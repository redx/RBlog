    <h3>关于原创性</h3>

<p>做这个世界上尚未存在的事物。</p>

<p>你可以被别人启发，启发的方式可能是文字、图像、声音、逻辑……但是你做事的目的不是为了生产某种已经在这个世界上存在的事物。</p>

<p>这个世界上有各种各样的灵感的原材料，上帝已经将他们准备好了放在那里，只是你是否能够遇到他们，用一种你自己的方式将这些灵感的原材料组合，产生独特的之前未曾在这个世界上存在过的事物。</p>

<h3>与他人的关系</h3>

<p>与平等的敬意去假设他人。</p>

<p>不要假设他人是白痴，相反，假设他们是非常聪明的人，他们有自己的直觉，那种直觉未必是你现在能够理解的。只是，他们可能很忙，所以他们或许不会留出足够多的时间，去思考你的创作。</p>

<p>所以你需要让一切能尽可能直抵核心，如果中途有什么出错了，那么你首先尝试去修复，如果无法在一个可靠的时间窗口内修复，这个时间窗口通常是 10 秒，那么，用尽量抓住要点的语言，描述自己遇到的错误，并且真诚致歉。</p>

<h3>关于钱</h3>

<p>细碎到几乎无法察觉的硬币的积累，胜过用直升飞机运输的一箱钞票。</p>

<p>认可 Tim O'Reilly 的那句话，人生是旅行，钱是汽油，旅程中需要加油，但汽油不是旅程的目的。</p>

<h3>是否开始</h3>

<p>最重要的事情是第一个星期的那些决定。而最初的那个决定最重要：是否开始这一切。</p>

<p>如果那个事物只能在世界上存在不到一年，一年之后便无人关心，那就绝对不要开始。</p>

<p>计算维持所需消耗的资源，这些消耗的资源对我们目前生活在的星球会造成什么样的影响？在其存在的过程中，谁或者什么的熵会因此而持续升高？有意义的存在是为了降低熵，那么你降低了谁或者什么的熵？</p>

<p>不以升高其他事物的熵为代价的降熵是否存在？这是你要探寻一生的问题。</p>

